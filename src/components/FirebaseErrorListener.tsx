'use client';

import { useState, useEffect } from 'react';
import { errorEmitter } from '@/firebase/error-emitter';
import { FirestorePermissionError } from '@/firebase/errors';

/**
 * An invisible component that listens for globally emitted 'permission-error' events.
 * It is a critical part of the development workflow for debugging Firestore Security Rules.
 * When a permission error is caught and emitted via the `errorEmitter`, this component
 * captures it and throws it. In a Next.js development environment, this thrown error
 * is then caught by Next.js's error overlay, displaying a detailed, developer-friendly
 * error message that includes the exact context of the failed security rule request.
 *
 * This component should render nothing and have no visual impact on the application.
 * Its sole purpose is to bridge the custom error emitting system with the Next.js
 * development error overlay.
 */
export function FirebaseErrorListener() {
  // Use the specific error type for the state for type safety.
  const [error, setError] = useState<FirestorePermissionError | null>(null);

  useEffect(() => {
    // The callback now expects a strongly-typed error, matching the event payload.
    const handleError = (error: FirestorePermissionError) => {
      // Set error in state to trigger a re-render.
      setError(error);
    };

    // The typed emitter will enforce that the callback for 'permission-error'
    // matches the expected payload type (FirestorePermissionError).
    errorEmitter.on('permission-error', handleError);

    // Unsubscribe on unmount to prevent memory leaks.
    return () => {
      errorEmitter.off('permission-error', handleError);
    };
  }, []);

  // On re-render, if an error exists in state, throw it.
  if (error) {
    throw error;
  }

  // This component renders nothing.
  return null;
}
